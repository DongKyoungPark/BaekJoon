#include <iostream>

using namespace std;

int main(void) {
	ios_base::sync_with_stdio(false);
	cin.tie(NULL);
	
	char s[100];

	cin >> s;
	for (char c = 'a'; c <= 'z'; c++) {
		int i = 0;
		int res = -1;
		while (s[i] != '\0')
		{
			if (s[i] == c) 
			{
				res = i; 
				break;
			}
			i++;
		}
		cout << res << " "; 
	}
	return 0;
}