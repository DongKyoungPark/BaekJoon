#include <iostream>

using namespace std;

int main(void)
{
	int a, b, c;
	int middle = 0;

	cin >> a >> b >> c;

	if (a >= b && a <= c || a <= b && a >= c)
		middle = a;
	else if (b >= a && b <= c || b <= a && b >= c)
		middle = b;
	else if (c >= a && c <= b || c <= a && c >= b)
		middle = c;

	cout << middle << endl;

	return 0;
}